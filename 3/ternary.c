#include <stdio.h>
int main() {
    int a =345;
    int b = 32;
    a>b? printf("a is greater than b\n") : printf("b is greater than a\n");

// Ternary operator syntax:
// condition ? expression_if_true : expression_if_false;
// if the condition is true, expression_if_true is executed; otherwise, expression_if_false is executed.
// this is a compact alternative to the if-else statement for simple conditions.


return 0;
}